public class BitOperationsTest19 {
    public static void main(String[] args) {
        int a = 4;

        System.out.println(a);

        System.out.println();

        System.out.println(a << 1);
        System.out.println(a << 2);

        System.out.println();

        System.out.println(a >> 1);
        System.out.println(a >> 2);
    }
}